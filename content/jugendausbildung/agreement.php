2.3 Sondertarife<br />
<br />
Geschwistererm&auml;ssigung ab dem 2. Kind von 25% der jeweiligen Geb&uuml;hr.<br />
Bei Besitz eines eigenen Instruments gew&auml;hrt der Verein einen Nachlass auf
die Unterrichtsgeb&uuml;hr von 50%. <br />
<br />
Die Ausbildung erfolgt, soweit dies m&ouml;glich ist durch vereinsangeh&ouml;rige
Musiker/innen, die entsprechend ihrer Qualifikation (JMLA in Silber f&uuml;r die
Blockfl&ouml;tengruppe, C-2 Ausbilder-Lehrgang f&uuml;r den Instrumentalunterricht).
Bei Bedarf werden weitere externe Lehrkr&auml;fte oder auch die Musikschule Murgtal
eingebunden.
<h4>Die Unterrichtsferien sind beitragspflichtig</h4>
Der Beitrag wird bei quartalsweise am Ende des Quartals vom angegebenen Konto abgebucht.<br />
<br />
<br />
<b>3. Unterricht</b><br />
<br />
Der Unterrichtstag und -ort wird in Absprache mit dem jeweiligen Ausbilder festgelegt.<br />
Unterrichtsart und - dauer richten sich nach Absprache mit dem Ausbilder nach dem jeweiligen Leistungstand des Sch&uuml;lers.<br />
Die Unterrichtsdauer betr&auml;gt in Regel bei Einzelunterricht 30 Minuten und bei Gruppenunterricht 60 Minuten. Voraussetzung f&uuml;r die Teilnahme am Unterricht ist der Abschluss der Ausbildervereinbarung des MVO.<br />
<br />
<br />
<b>4. Instrumente, Lehrmittel</b><br />
<br />
Instrumente werden gegen eine geringe Leihgeb&uuml;hr f&uuml;r die Zeit der Ausbildung bis zum Eintritt in die Gesamtkapelle) vom Verein zur Verf&uuml;gung gestellt. Da es sich &uuml;berwiegend um gut erhaltenen Instrumente handelt, sind diese mit der entsprechenden Sorgfalt zu versehen. Kosten f&uuml;r &uuml;blich Instand- Wartungsaufwendungen werden vom Verein &uuml;bernommen.<br />
<br />
<font style='color:#A00000;'>Sch&auml;den, die durch grob fahrl&auml;ssigen Umgang entstehen, sind vom Verursacher zu &uuml;bernehmen.</font><br />
<br />
Der Musikverein Reichental bietet interessierten Eltern auch die M&ouml;glichkeit des Instrumentenmietkaufs an. Alternativ hierzu besteht die M&ouml;glichkeit des Erwerbs eines neuen oder gebrauchten Instruments. Hierbei erwirbt der Verein das Instrument und ver&auml;ussert es zum Anschaffungspreis weiter.<br />
<br />
<br />
<b>5. Pflichten des Sch&uuml;lers, bzw. Erziehungsberechtigten</b><br />
<br />
Die Sch&uuml;ler verpflichten sich zum regelm&auml;ssigen und p&uuml;nktlichen Besuch des Unterrichts.<br />
Bei Erkrankung oder Verhinderung ist der zust&auml;ndige Ausbilder rechtzeitig zu benachrichten. <br />
<br />
<br />
<b>6. Allgemeines</b><br />
<br />
Mit Unterschrift der Vereinbarung erkennen die Auszubildenden / Erziehungsberechtigten dieselbe einschliesslich der Geb&uuml;hrenordnung an.<br />
<br />
Abmeldungen sind fr&uuml;hestens zu Quartalsende m&ouml;glich und m&uuml;ssen sp&auml;testens einen Monat zuvor schriftlich oder m&uuml;ndlich beim 1. Vorstand oder Jugendleiter/in eingegangen sein. In Ausnahmenf&auml;llen (Wohnortwechsel, l&auml;nger andauernde Krankheit) sind Ausnahmen m&ouml;glich.<br />
<p>Ich bin mir bewusst, dass im Rahmen der Ausbildung Bilder von Veranstaltungen auf der Homepage des Musikvereins ver&ouml;ffentlicht werden.</p>
<p><b>Die Geb&uuml;hrenordnung setzt voraus, dass zumindest ein Erziehungsberechtigter Mitglied des Vereins ist.</b></p>
