<b>1.Ausbildungsziel</b><br /><br />
Ziel der Ausbildung ist die Vermittlung der musikalischen Grundausbildung an einem Blas- oder Schlaginstrument mit dem erkl&auml;rten Ziel der aktiven Mitwirkung in der Jugend- bzw. Gesamtkapelle des Musikvereins "Orgelfels" Reichental (MVO).<br />
Vorausetzung f&uuml;r die Mitwirkung in der Gesamtkapelle ist der Erwerb des Jungmusikerleistungsabzeichens (JMLA) in Bronze.<br />
Sollte das Ausbildungsziel "&Uuml;bernahme in die Musikkapelle" nicht erreicht werden, behalten wir uns vor, die Kosten der Ausbildung anteilsm&auml;&szlig;ig zur&uuml;ckzufordern.
