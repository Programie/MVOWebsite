<h2>Die Gr&uuml;ndung des Vereins</h2>
Im November 1954 folgten viele Reichentaler einer besonderen Einladung in den "Gr&#252nen Baum".
Aus der Musikkapelle sollte ein Musikverein werden. <br />
W&auml;hrend die Kapelle ein St&#252ck nach dem anderen spielte, wuchs eine Liste,
die bereits die Runde machte, auf stattliche 75 Mitglieder an.<br />
Aber es bedurfte einer 2. Gr&#252nderversammlung, bis die Vorstandschaft und der
Name des Vereins feststanden. &Uuml;ber den Vereinsnamen wurde lange diskutiert.
Schlie&szlig;lich gab eine Felsformation dem Verein den Namen.
Der Orgelfelsen, aus geologischer Sicht entstanden durch "Wollsackverwitterung",
liegt oberhalb Reichentals, und man verband mit dem Felsen diesen Wunsch:
"M&#246ge die Musikkapelle Orgelfels wie eine Orgel ihre Kl&#228nge ert&#246nen lassen,
und m&#246ge der Verein wie ein Fels immerfort bestehen bleiben."
<br /><br />
Nach 36 Jahren als Vorstand &uuml;bergab am 10. Februar 1963 Kornel Merkel das Amt
des Vorsitzenden an seinen Sohn Helmut Merkel. Kornel Merkel wurde noch am
gleichen Abend zum Ehrenvorsitzenden ernannt.
<br /><br />
Wie alte Fotos zeigen traten die Musiker anfangs in ihrem privaten Sonntagsstaat auf.
Eine blaue Schildkappe sorgte sp&#228ter f&uuml;r ein einheitlicheres Bild. In den f&uuml;nfziger Jahren
pr&#228sentierten sich die Musiker in schwarzen Anz&#252gen mit einer verzierten Schildm&uuml;tze.

