von den verkleideten Musikern angef&#252hrt wird, schloss sich eine Kost&#252mpremierung f&#252r
die Kinder an. Am Abend wurde der erste Rosenmontagsball mit einer Tanzkapelle gefeiert.
Ein Jahr sp&#228ter wurde auch am Fastnachtsonntag zum Tanz aufgespielt.
